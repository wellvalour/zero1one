package com.gruppezwei.zero1one.controller;

public class Passwort {

	String PWAlt;
	
	String PWNeu;
	
	String PWNeuWdh;

	public String getPWAlt() {
		return PWAlt;
	}

	public void setPWAlt(String pWAlt) {
		PWAlt = pWAlt;
	}

	public String getPWNeu() {
		return PWNeu;
	}

	public void setPWNeu(String pWNeu) {
		PWNeu = pWNeu;
	}

	public String getPWNeuWdh() {
		return PWNeuWdh;
	}

	public void setPWNeuWdh(String pWNeuWdh) {
		PWNeuWdh = pWNeuWdh;
	}
	
	
	
}
